let rating = 6;

switch(rating){

    case 5:
        console.log("great");
        break;
    
    case 4:
        console.log("very good");
        break;

    case 3:
        console.log("good");
        break;

    case 2:
        console.log("poor");
        break;
    
    case 1:
        console.log("very bad");
        break;

    default:
        console.log("INVALID");
}